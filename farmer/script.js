var side = 15;

function setup() {
    //matrix = generateMatrix(30);
    createObjects();
    frameRate(60);
    createCanvas(matrix[0].length * side, matrix.length * side);
    background('#acacac');
}

function draw() {

    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {

            if (matrix[y][x] == 1) {
                fill("green");
            }
            else if (matrix[y][x] == 2) {
                fill("yellow");
            }
            else if (matrix[y][x] == 3) {
                fill("red");
            }
            else if (matrix[y][x] == 4) {
                let pigColor = color(255, 106, 106);
                fill(pigColor);
            }
            else if (matrix[y][x] == 5) {
                let mudColor = color(71, 48, 0);
                fill(mudColor);
            }
            else {
                fill("gray");
            }

            rect(x * side, y * side, side, side);
        }
    }

    for (var i in grassArr){
        grassArr[i].mul();
    }
    for (var i in grassEaterArr){
        grassEaterArr[i].eat();
    }
    for (var i in farmerArr){
        farmerArr[i].move();
    }
    for (var i in pigArr){
        pigArr[i].move();
    }
    for (var i in mudArr){
        mudArr[i].die();
    }
}

